package hr.fer.zemris.java.nescume.messages.server.exceptions;

/**
 * Opća iznimka koju server može baciti.
 */
public class ServerException extends Exception {

	private static final long serialVersionUID = 1L;
	
	/**
	 * Konstruktor koji stvara novu iznimku.
	 */
	public ServerException() {

	}
	
	/**
	 * Konstruktor koji stvara novu iznimku i prima razlog.
	 * @param reason razlog zašto je iznimka bačena
	 */
	public ServerException(String reason) {
		super(reason);
	}
	
	/**
	 * Konstruktor koji stvara novu iznimku i prima razlog i uzrok.
	 * @param reason razlog zašto je iznimka bačena
	 * @param cause uzrok radi kojeg je iznimka bačena
	 */
	public ServerException(String reason, Throwable cause) {
		super(reason, cause);
	}
}
